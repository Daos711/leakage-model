"""Коэффициент трения Дарси по формуле Черчилля."""

import math


def churchill_friction(Re: float, eps_D: float = 0.0) -> float:
    """Коэффициент Дарси по формуле Черчилля.

    Универсальная аппроксимация для широкого диапазона Re,
    не требует итераций.

    Parameters
    ----------
    Re : float
        Число Рейнольдса.
    eps_D : float
        Относительная шероховатость (0 для гладких труб).

    Returns
    -------
    float
        Коэффициент трения Дарси f.
    """
    if Re < 1:
        return 64.0  # формальная заглушка

    A = (-2.457 * math.log((7 / Re) ** 0.9 + 0.27 * eps_D)) ** 16
    B = (37530 / Re) ** 16
    f = 8 * ((8 / Re) ** 12 + 1 / (A + B) ** 1.5) ** (1 / 12)
    return f
