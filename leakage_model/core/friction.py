"""Формула Черчилля — коэффициент трения Дарси-Вейсбаха."""

import numpy as np

# Параметры верхнего участка ствола (по умолчанию)
EPS_DEFAULT = 0.002  # м, шероховатость стенок (бетон)


def churchill_friction(Re, D, eps=EPS_DEFAULT):
    """Коэффициент трения λ по формуле Черчилля (все режимы течения).

    Параметры
    ---------
    Re : float или ndarray
        Число Рейнольдса
    D : float
        Диаметр трубы, м
    eps : float
        Абсолютная шероховатость, м

    Возвращает
    ----------
    float или ndarray
        Коэффициент трения Дарси-Вейсбаха λ
    """
    Re = np.maximum(np.abs(np.asarray(Re, dtype=float)), 1.0)
    e_D = eps / D

    A = (-2.457 * np.log((7.0 / Re) ** 0.9 + 0.27 * e_D)) ** 16
    B = (37530.0 / Re) ** 16
    f = 8.0 * ((8.0 / Re) ** 12 + 1.0 / (A + B) ** 1.5) ** (1.0 / 12.0)
    return float(f) if np.ndim(f) == 0 else f
