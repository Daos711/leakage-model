def load_plates_data() -> pd.DataFrame:
    """Загрузить данные по пластинам как DataFrame.

    Возвращает DataFrame с колонками:
        insert_id, insert_name, u1, PUV, r_baseline, r_exp
    """
    rows = []
    for insert_id, info in PLATES_PUV.items():
        for u1, puv in info["puv"]:
            r_base = R_BASELINE.get(u1)
            if r_base is not None:
                rows.append({
                    "insert_id": insert_id,
                    "insert_name": info["name"],
                    "u1": u1,
                    "PUV": puv,
                    "r_baseline": r_base,
                    "r_exp": r_base * puv,
                })
    return pd.DataFrame(rows)


# ---------------------------------------------------------------------------
# Парсинг геометрии вставки из названия
# ---------------------------------------------------------------------------

_RE_INSERT_NUM = re.compile(r"ВСТАВКА\s*[№#]?\s*(\d+)", re.IGNORECASE)
_RE_SERIES = re.compile(r"(\d+)\.(\d+)\s*=")
_RE_PLATES = re.compile(r"(\d+)\s*[хxXХ×]\s*(\d+)\s*°?")
_RE_WIDTH = re.compile(r"(\d+)\s*мм")
_RE_RING = re.compile(r"90°|1х90|1[хx]90", re.IGNORECASE)
_RE_NONE = re.compile(r"без\s*пластин|0х.*0\s*мм", re.IGNORECASE)
_RE_OBLIQUE = re.compile(r"наискосок", re.IGNORECASE)


def parse_insert_geometry(insert_name: str) -> dict:
    """Разобрать название вставки в структурированные признаки.

    Возвращает dict с полями:
      insert_num   — номер вставки
      series_id    — номер серии (1, 2, 3, 4)
      n_plates     — количество пластин
      angle_deg    — угол наклона пластины
      width_mm     — ширина пластины (мм)
      topology     — тип: 'none' / 'ring' / 'inclined' / 'mixed' / 'unknown'
    """
    result = {
        "insert_num": 0,
        "series_id": 0,
        "n_plates": 0,
        "angle_deg": 0,
        "width_mm": 0,
        "topology": "unknown",
    }

    m = _RE_INSERT_NUM.search(insert_name)
    if m:
        result["insert_num"] = int(m.group(1))

    m = _RE_SERIES.search(insert_name)
    if m:
        result["series_id"] = int(m.group(1))

    # Ширина
    mw = _RE_WIDTH.search(insert_name)
    if mw:
        result["width_mm"] = int(mw.group(1))

    # Количество пластин и угол
    mp = _RE_PLATES.search(insert_name)
    if mp:
        result["n_plates"] = int(mp.group(1))
        result["angle_deg"] = int(mp.group(2))

    # Определить топологию
    if result["insert_num"] == 1:
        result["topology"] = "none"
    elif _RE_OBLIQUE.search(insert_name):
        result["topology"] = "inclined"
    elif _RE_RING.search(insert_name) and "пл-ть" in insert_name.lower():
        result["topology"] = "mixed"
    elif _RE_RING.search(insert_name):
        result["topology"] = "ring"
    elif result["n_plates"] > 0:
        result["topology"] = "inclined"
    else:
        logger.warning("Не удалось разобрать геометрию: %s", insert_name)

    return result


def load_plates_with_geometry() -> pd.DataFrame:
    """load_plates_data + parse_insert_geometry для каждой вставки.

    Возвращает DataFrame с дополнительными колонками геометрии.
    """
    df = load_plates_data()

    geom_cols = ["insert_num", "series_id", "n_plates", "angle_deg", "width_mm", "topology"]
    for col in geom_cols:
        df[col] = 0 if col != "topology" else ""

    insert_names = df.drop_duplicates("insert_id")[["insert_id", "insert_name"]]
    for _, row in insert_names.iterrows():
        geom = parse_insert_geometry(row["insert_name"])
        mask = df["insert_id"] == row["insert_id"]
        for col in geom_cols:
            df.loc[mask, col] = geom[col]

    for col in ["insert_num", "series_id", "n_plates", "angle_deg", "width_mm"]:
        df[col] = df[col].astype(int)

    return df
